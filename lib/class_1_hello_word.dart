void main(){
  print("Hello world");
}


// void (The Return Type)
// This keyword tells Dart that this function does not return any value. It performs an action (printing text) and then finishes. If the function was going to give back a number, you would use int instead of void.

// 2. main() (The Function Name)
// This is a special reserved name. When you run your program, the Dart Virtual Machine looks specifically for a function named main.

// The parentheses () are used to hold parameters (inputs), though in this simple version, they are empty.

// 3. { ... } (The Code Block)
// The curly braces define the "body" of the function. Everything inside these braces is what the computer will actually do when the function is called.

// 4. print("Hello world"); (The Instruction)
// print: This is a built-in function that tells the computer to display whatever is inside the parentheses to the console (terminal).

// "Hello world": This is a String (text). In Dart, Strings must be wrapped in either single quotes ' or double quotes ".

// ; (The Semicolon): Think of this as a "period" at the end of a sentence. In Dart, every standalone instruction must end with a semicolon to tell the compiler the statement is finished.